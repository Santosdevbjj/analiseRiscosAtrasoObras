        "                'id_atividade': id_atv,\n",
        "                'id_fornecedor': forn['id_fornecedor'],\n",
        "                'material': material,\n",
        "                'atrasou_entrega': atraso_suprimento\n",
        "            })\n",
        "\n",
        "df_atividades = pd.DataFrame(atividades)\n",
        "df_suprimentos = pd.DataFrame(suprimentos)\n",
        "\n",
