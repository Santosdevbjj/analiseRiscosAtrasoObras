        "        'id_obra': f'MRV-{100+i}',\n",
        "        'nome_empreendimento': f'Residencial {fake.street_name()}',\n",
        "        'cidade': random.choice(cidades),\n",
        "        'orcamento_estimado': round(random.uniform(5_000_000, 20_000_000), 2),\n",
        "        'data_inicio_prevista': fake.date_between(start_date='-1y', end_date='today')\n",
        "    })\n",
        "\n",
        "df_obras = pd.DataFrame(obras)\n",
        "df_obras['data_inicio_prevista'] = pd.to_datetime(df_obras['data_inicio_prevista'])\n",
        "\n",
        "# 2. Gerar Fornecedores\n",
        "fornecedores = []\n",
        "for i in range(NUM_FORNECEDORES):\n",
